let provider;
let signer;
let contract;

async function connectWallet() {
    if (typeof window.ethereum !== 'undefined') {
        provider = new ethers.providers.Web3Provider(window.ethereum);
        signer = provider.getSigner();
        await provider.send("eth_requestAccounts", []);
        contract = new ethers.Contract(contractAddress, contractABI, signer);
        alert('Wallet connected!');
    } else {
        alert('Please install MetaMask!');
    }
}

async function mintTokens() {
    const address = document.getElementById('mintAddress').value;
    const amount = document.getElementById('mintAmount').value;
    try {
        const tx = await contract.mint(address, amount);
        await tx.wait();
        alert('Tokens minted successfully!');
    } catch (error) {
        console.error(error);
        alert('Error minting tokens');
    }
}

async function transferTokens() {
    const address = document.getElementById('transferAddress').value;
    const amount = document.getElementById('transferAmount').value;
    try {
        const tx = await contract.transferTokens(address, amount);
        await tx.wait();
        alert('Tokens transferred successfully!');
    } catch (error) {
        console.error(error);
        alert('Error transferring tokens');
    }
}

async function burnTokens() {
    const amount = document.getElementById('burnAmount').value;
    try {
        const tx = await contract.burnTokens(amount);
        await tx.wait();
        alert('Tokens burned successfully!');
    } catch (error) {
        console.error(error);
        alert('Error burning tokens');
    }
}

async function redeemTokens() {
    const amount = document.getElementById('redeemAmount').value;
    const input = document.getElementById('redeemInput').value;
    try {
        const tx = await contract.redeemTokens(amount, input);
        await tx.wait();
        alert('Tokens redeemed successfully!');
    } catch (error) {
        console.error(error);
        alert('Error redeeming tokens');
    }
}

async function checkBalance() {
    const address = document.getElementById('balanceAddress').value;
    try {
        const balance = await contract.balanceOf(address);
        document.getElementById('balanceResult').innerText = `Balance: ${balance}`;
    } catch (error) {
        console.error(error);
        alert('Error checking balance');
    }
}

window.onload = function () {
    connectWallet();
};
